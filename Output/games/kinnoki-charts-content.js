const toSolution = (rows) => rows.flatMap((row) => [...row].map((glyph) => (glyph === 'X' ? 1 : 0)));

const withSolutions = (entries) => entries.map(({ id, title, rows }) => ({
  id, title, size: rows.length, solution: toSolution(rows),
}));

export const CHART_CATALOG = Object.freeze({
  easy: Object.freeze(withSolutions(RAW_CATALOG.easy)),
  medium: Object.freeze(withSolutions(RAW_CATALOG.medium)),
  hard: Object.freeze(withSolutions(RAW_CATALOG.hard)),
});
